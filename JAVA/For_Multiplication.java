
public class For_Multiplication {
     public static void main(String[] args) {
         System.out.println("Multiplication Table ");
         int a,i,j;
         for (i=1; i<7;i++){
         for (j=1;j<7;j++){
         a=j*i;
             System.out.print("  "+a);
         }
         System.out.println("");
     }
     }
}
